class Program
{
    static void Main()
    {
        // Создаем массив объектов (гирь) и присваиваем им вес
        int[] giri = { 10, 5, 15, 20, 7 };

        // Инициализируем переменную для хранения наибольшего веса и присваиваем ей значение первого элемента массива
        int maxWeight = giri[0];

        // Проходим по всем элементам массива и сравниваем с максимальным весом
        for (int i = 1; i < giri.Length; i++)
        {
            if (giri[i] > maxWeight)
            {
                maxWeight = giri[i];
            }
        }

        // Выводим наибольший вес
        Console.WriteLine($"Наибольший вес гири: {maxWeight}");
    }
}